    <script>
        // Generate equipment cards
        function generateCards(data) {
            const grid = document.getElementById('equipmentGrid');
            grid.innerHTML = '';
            
            data.forEach((equipment, index) => {
                const card = document.createElement('div');
                card.className = 'equipment-card';
                card.dataset.difficulty = equipment.difficulty;
                card.onclick = () => openModal(equipment);
                
                const difficultyText = {
                    'beginner': 'Pemula',
                    'intermediate': 'Menengah',
                    'advanced': 'Lanjutan'
                };
                
                card.innerHTML = `
                    <div class="card-header">
                        <h3>${equipment.name}</h3>
                        <div class="difficulty">${difficultyText[equipment.difficulty]}</div>
                    </div>
                    <div class="card-body">
                        <div class="muscle-groups">
                            ${equipment.muscles.map(muscle => `<span class="muscle-tag">${muscle}</span>`).join('')}
                        </div>
                        <p><strong>Kategori:</strong> ${equipment.category}</p>
                        <p><strong>Jumlah Langkah:</strong> ${equipment.steps.length}</p>
                    </div>
                `;
                
                grid.appendChild(card);
            });
        }

        // Open modal with equipment details
        function openModal(equipment) {
            const modal = document.getElementById('equipmentModal');
            const modalContent = document.getElementById('modalContent');
            
            const difficultyText = {
                'beginner': 'Pemula',
                'intermediate': 'Menengah',
                'advanced': 'Lanjutan'
            };
            
            modalContent.innerHTML = `
                <h2>${equipment.name}</h2>
                <p><strong>Tingkat Kesulitan:</strong> <span class="difficulty">${difficultyText[equipment.difficulty]}</span></p>
                <div class="muscle-groups">
                    <strong>Target Otot:</strong><br>
                    ${equipment.muscles.map(muscle => `<span class="muscle-tag">${muscle}</span>`).join('')}
                </div>
                
                <div class="steps">
                    <h3>Langkah-langkah Penggunaan:</h3>
                    ${equipment.steps.map((step, index) => `
                        <div class="step">
                            <span class="step-number">${index + 1}</span>
                            ${step}
                        </div>
                    `).join('')}
                </div>
                
                <div class="tips">
                    <div class="tips-title">üí° Tips:</div>
                    ${equipment.tips}
                </div>
                
                <div class="warning">
                    <div class="warning-title">‚ö†Ô∏è Peringatan:</div>
                    ${equipment.warning}
                </div>
            `;
            
            modal.style.display = 'block';
        }

        // Search functionality
        document.getElementById('searchBox').addEventListener('input', function(e) {
            const searchTerm = e.target.value.toLowerCase();
            const filteredData = equipmentData.filter(equipment => 
                equipment.name.toLowerCase().includes(searchTerm) ||
                equipment.category.toLowerCase().includes(searchTerm) ||
                equipment.muscles.some(muscle => muscle.toLowerCase().includes(searchTerm))
            );
            generateCards(filteredData);
        });

        // Filter functionality
        document.querySelectorAll('.filter-btn').forEach(btn => {
            btn.addEventListener('click', function() {
                // Remove active class from all buttons
                document.querySelectorAll('.filter-btn').forEach(b => b.classList.remove('active'));
                // Add active class to clicked button
                this.classList.add('active');
                
                const filter = this.dataset.filter;
                let filteredData = equipmentData;
                
                if (filter !== 'all') {
                    filteredData = equipmentData.filter(equipment => equipment.difficulty === filter);
                }
                
                generateCards(filteredData);
            });
        });

        // Modal functionality
        document.querySelector('.close').addEventListener('click', function() {
            document.getElementById('equipmentModal').style.display = 'none';
        });

        window.addEventListener('click', function(e) {
            const modal = document.getElementById('equipmentModal');
            if (e.target === modal) {
                modal.style.display = 'none';
            }
        });

        // Initialize page
        document.addEventListener('DOMContentLoaded', function() {
            generateCards(equipmentData);
        });
